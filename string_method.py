name = "john"
#character of squence
strLength = len(name)
print("strLength : ",strLength)
print(name.upper()) #convert the string to uppercase
print(name.lower()) #convert the string to lowercase
print(name.capitalize()) #convert the first character to uppercase
print(name.title()) #convert the first character of each word to uppercase
print(name.swapcase()) #swap the case of the string
print(name.isupper()) #check if the string is uppercase
print(name.islower()) #check if the string is lowercase


